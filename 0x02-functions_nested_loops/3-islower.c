/**
 * _islower - prints lowercase letters
 * c - is a interger of lower case
 * Return: 1 if c is a lowercase and 0 if otherwise
 */
int _islower(int c) /*c is an interger that represent lower case*/
{
	if (c == 'c')
	{
		return (1);
	}
	else if (c == 'o')
	{
		return (1);
	}
	else if (c == 'l')
	{
		return (1);
	}
	else if (c == 's')
	{
		return (1);
	}
	else if (c == 'n')
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
